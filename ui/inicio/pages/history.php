        <h3>  Historia del club KOPULSO</h3>
        <p>
            El Club de Taekwondo KOPULSO tiene más de dos décadas de trayectoria formando guerreros en el corazón de Bogotá. Su historia comenzó en 1998 cuando la Maestra Fundadora <i>Ángela Morales</i>, campeona en Taekwondo y Full Contact, comenzó a compartir su pasión por este arte marcial con los estudiantes de la Universidad Antonio Nariño. Morales, discípula del Maestro Nicolás, fundador del Club Jan Su Do, logró transmitir sus conocimientos con dedicación, formando a una nueva generación de practicantes.
        </p>
            
        </p>
        <p>
            En 1999, tras haber consolidado su experiencia, la Maestra Ángela Morales decidió emprender su propio camino junto con la Maestra Lucía Patiño, y juntas fundaron el Club de Taekwondo KOPULSO (que significa “El Club del Rinoceronte”), en el barrio San José de Bogotá. Bajo este nuevo nombre, el club creció rápidamente, formando una verdadera "casta" de luchadores conocidos como los "Rinos", quienes no solo sobresalían por su habilidad técnica, sino también por su disciplina, ética y espíritu competitivo.
        </p>
        <div class="separator"></div>
        <p>
            <br>
            A lo largo de los años, KOPULSO ha formado a múltiples campeones distritales y nacionales, ganando reputación como uno de los clubes con los mejores competidores de alto nivel en la ciudad. El club se ha destacado por su ambiente de camaradería, respeto mutuo y un fuerte enfoque en los valores que el Taekwondo inculca, como la perseverancia, el autocontrol y el respeto.
        </p>
            
        <p>
            En abril de 2017, la Liga de Taekwondo de Bogotá honró a la Maestra Ángela Morales con el 5° DAN, reconociendo su invaluable contribución al crecimiento y desarrollo del Taekwondo en la capital. Este reconocimiento no solo refleja su éxito como competidora y entrenadora, sino también su incansable labor por consolidar el Taekwondo en Bogotá como una disciplina que trasciende lo deportivo y llega a lo formativo.
        </p>
        <p>
            Lamentablemente, en septiembre de 2017, la Maestra Ángela Morales falleció por razones de salud, dejando un legado inmenso tanto en el Taekwondo como en las vidas de quienes la conocieron. Sin embargo, su legado no se detuvo ahí. Desde su partida, los profesores Marcela González, Jhon Ávila y Argemiro Pardo han continuado con su misión, preservando y replicando las enseñanzas de la Maestra Morales, manteniendo viva la filosofía y el espíritu guerrero de los "Rinocerontes". Ellos, con el mismo compromiso y pasión, siguen instruyendo a nuevas generaciones de practicantes, formando no solo campeones en el tatami, sino también en la vida.
        </p>
